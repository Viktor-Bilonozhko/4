#include <stdio.h>
#include <math.h>

int main() {
    int n; // число, для якого потрібно знайти кількість рівних дільників
    scanf("%d", &n);
    int count = 0; // змінна для зберігання кількості рівних дільників
    for (int i = 2; i <= sqrt(n); i++) {
        if (n % i == 0 && n / i == n % i) { // перевірка умови рівного дільника
            count++; // збільшення лічильника
        }
    }
    printf("%d\n", count); // виведення кількості рівних дільників
    return 0;
}
